public class While
{
    public static void main(String args [])
    {
        int count = 0;
        while (count <= 500)
        {
            if ((count%5) == 0 && (count%7) == 0) System.out.println(count + " is a multiple of 5 and 7");
            else if ((count%5) == 0 || (count%7) == 0) System.out.println(count + " is a multiple of 5 or 7");
            count++;
        }
    }
}
