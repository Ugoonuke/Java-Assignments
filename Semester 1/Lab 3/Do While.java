public class DoWhile
{
    public static void main(String args [])
    {
        int num = 13;
        int a = 1, count = 0;
        do
        {
            if ((num % a) == 0)
            {
                count++;
            }
            a++;
        }
        while (a <= num);
        if (count == 2)
        {
            System.out.println(num +" is a prime number");
        }
        else
        {
            System.out.println(num +" is NOT a prime number");
        }
    }
}
